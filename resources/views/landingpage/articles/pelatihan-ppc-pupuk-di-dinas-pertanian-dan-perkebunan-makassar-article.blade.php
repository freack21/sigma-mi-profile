  Pelatihan PPC Pupuk di Dinas Pertanian dan Perkebunan Makassar
@endsection

@section('article_body')
  <p>
    Pelatihan ini bertujuan untuk memberikan pengetahuan dan keterampilan dalam pengambilan contoh bahan atau barang kepada petugas pengambil contoh yang bersertifikasi dan kompeten secara keahlian. Petugas pengambil contoh yang kompeten menjadi garda terdepan dalam melakukan analisa mutu pupuk dalam pemenuhan persyaratan regulasi peraturan Kementerian Pertanian.
  </p>

  <h2>Materi Pelatihan</h2>
  <ul>
    <li><p>
      Metode pengambilan contoh sesuai skema sertifikasi
    </p></li>
    <li><p>
      Cara menetapkan dan menerapkan metode pengambilan contoh di lapangan
    </p></li>
    <li><p>
      Cara penanganan, pengemasan, dan distribusi unit contoh
    </p></li>
    <li><p>
      Praktik lapangan pengambilan contoh
    </p></li>
  </ul>

  <h2>Manfaat Pelatihan</h2>
  <ul>
    <li><p>
      Meningkatkan kapasitas SDM
    </p></li>
    <li><p>
      Mendukung keputusan berbasis data dan pengawasan kualitas mutu
    </p></li>
    <li><p>
      Mendapatkan data informasi lapangan terkait kualitas dan keamanan pangan
    </p></li>
  </ul>

  <h2>Syarat dan Kompetensi</h2>
  <ul>
    <li><p>
      Memiliki kompetensi di bidang pengambilan contoh
    </p></li>
    <li><p>
      Memahami metode pengambilan contoh yang sesuai dengan standar
    </p></li>
    <li><p>
      Memiliki sertifikat kompetensi yang dikeluarkan oleh lembaga yang berwenang
    </p></li>
  </ul>

  <h2>Contoh Kegiatan Pengambilan Contoh</h2>
  <ul>
    <li><p>
      Pengambilan contoh pupuk organik cair (POC) dan Humid Acid
    </p></li>
    <li><p>
      Pengambilan contoh produk Urea N (Nitrogen) 46%
    </p></li>
  </ul>

