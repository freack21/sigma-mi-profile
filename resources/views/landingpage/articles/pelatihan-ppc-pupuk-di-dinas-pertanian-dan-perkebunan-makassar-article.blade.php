    </p></li>
    <li><p>
      Buka jahitan/segel kemasan dan ambil contoh
    </p></li>
    <li><p>
      Gabungkan semua contoh primer yang telah diambil (komposit)
    </p></li>
    <li><p>
      Lakukan pembagian contoh (quartering)
