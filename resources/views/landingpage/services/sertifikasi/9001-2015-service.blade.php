  Sertifikasi ISO 9001:2015
@endsection

@section('article_body')

  <p>
    ISO 9001:2015 adalah standar internasional yang menetapkan persyaratan untuk sistem manajemen mutu (SMM) di suatu organisasi. Standar ini dirancang untuk membantu organisasi dalam memastikan bahwa produk dan layanan yang diberikan dapat memenuhi kebutuhan pelanggan serta peraturan yang berlaku. ISO 9001:2015 adalah versi terbaru dari ISO 9001, yang sebelumnya diterbitkan pada tahun 2008, dan membawa sejumlah pembaruan penting.
  </p>

  <img src="{{ asset('img/gallery/sertifikasi-iso-9001-2015-di-pt-asa-kreasi-global-1.jpg') }}" alt="Ilustrasi">
  <p class="caption">Sertifikasi ISO 9001:2015 di PT. Asa Kreasi Global (1)</p>

  <h4>
    1. Fokus pada Kepuasan Pelanggan
  </h4>
  <p>
    Salah satu prinsip utama ISO 9001:2015 adalah peningkatan kepuasan pelanggan. Organisasi diharuskan untuk memahami kebutuhan dan harapan pelanggan serta berusaha untuk memenuhi atau bahkan melebihi harapan tersebut. Hal ini mencakup pemantauan kepuasan pelanggan dan tindak lanjut untuk perbaikan berkelanjutan.
  </p>

  <h4>
    2. Pendekatan Proses
  </h4>
  <p>
    ISO 9001:2015 menekankan pada pendekatan berbasis proses dalam manajemen mutu. Ini berarti bahwa organisasi harus mengelola proses secara sistematis untuk mencapai hasil yang diinginkan, meminimalkan variabilitas, dan meningkatkan efisiensi. Setiap proses harus dipantau, dikendalikan, dan ditingkatkan seiring waktu.
  </p>

  <h4>
    3. Kepemimpinan yang Kuat
  </h4>
  <p>
    Dalam ISO 9001:2015, terdapat penekanan lebih besar pada peran kepemimpinan dalam sistem manajemen mutu. Manajemen puncak diharuskan untuk menunjukkan komitmen terhadap sistem manajemen mutu, menetapkan visi dan arah organisasi, serta memastikan sumber daya yang cukup tersedia untuk mendukung pencapaian tujuan kualitas.
  </p>

  <h4>
    4. Manajemen Risiko
  </h4>
  <p>
    Salah satu pembaruan penting dalam ISO 9001:2015 adalah pendekatan berbasis risiko. Organisasi diwajibkan untuk mengidentifikasi dan mengelola risiko yang dapat memengaruhi pencapaian tujuan mutu dan kesuksesan organisasi. Pendekatan ini membantu organisasi untuk lebih proaktif dalam mencegah masalah, bukan hanya bereaksi terhadapnya setelah terjadi.
  </p>

  <img src="{{ asset('img/gallery/sertifikasi-iso-9001-2015-di-pt-asa-kreasi-global-2.jpg') }}" alt="Ilustrasi">
  <p class="caption">Sertifikasi ISO 9001:2015 di PT. Asa Kreasi Global (2)</p>

  <h4>
    5. Pemikiran Berbasis Nilai Tambah
  </h4>
  <p>
    ISO 9001:2015 mengedepankan pentingnya mengidentifikasi dan mengeliminasi aktivitas yang tidak memberikan nilai tambah bagi pelanggan. Hal ini bertujuan untuk meningkatkan efisiensi dan efektivitas operasional dengan fokus pada proses yang memberikan manfaat nyata.
  </p>

  <h4>
    6. Keterlibatan Semua Pihak Terkait
  </h4>
  <p>
    ISO 9001:2015 mendorong keterlibatan semua pihak yang berkepentingan dalam penerapan sistem manajemen mutu. Ini termasuk karyawan, pemasok, dan pihak lain yang berkontribusi pada pencapaian hasil yang diinginkan. Keterlibatan ini memastikan bahwa setiap orang memiliki pemahaman yang jelas mengenai peran dan tanggung jawab mereka dalam sistem manajemen mutu.
  </p>

  <h4>
    7. Peningkatan Berkelanjutan
  </h4>
  <p>
    Peningkatan berkelanjutan tetap menjadi inti dari ISO 9001:2015. Organisasi harus terus berupaya meningkatkan efektivitas dan efisiensi sistem manajemen mutu mereka dengan melakukan evaluasi dan perbaikan secara terus-menerus.
  </p>

  <h4>
    8. Dokumentasi yang Sederhana
  </h4>
  <p>
    ISO 9001:2015 lebih fleksibel dalam hal dokumentasi dibandingkan dengan versi sebelumnya. Organisasi dapat menyederhanakan sistem dokumentasi mereka untuk mencerminkan kebutuhan dan ukuran organisasi mereka, dengan tetap memenuhi persyaratan standar ini.
  </p>

  <img src="{{ asset('img/gallery/sertifikasi-iso-9001-2015-di-pt-asa-kreasi-global-4.jpg') }}" alt="Ilustrasi">
  <p class="caption">Sertifikasi ISO 9001:2015 di PT. Asa Kreasi Global (4)</p>

  <h4>
    9. Audit Internal dan Evaluasi Kinerja
  </h4>
  <p>
    Organisasi diharuskan untuk melakukan audit internal dan mengevaluasi kinerja sistem manajemen mutu mereka secara teratur. Hal ini penting untuk memastikan bahwa sistem berfungsi sesuai dengan rencana dan bahwa ada kesempatan untuk perbaikan berkelanjutan.
  </p>

  <h4>
    10. Kesimpulan
  </h4>
  <p>
    ISO 9001:2015 memberikan panduan yang jelas bagi organisasi untuk meningkatkan kualitas produk dan layanan mereka, sekaligus mengelola risiko dan meningkatkan kepuasan pelanggan. Implementasi sistem manajemen mutu yang sesuai dengan standar ini dapat memberikan manfaat besar bagi organisasi, seperti efisiensi yang lebih baik, reputasi yang lebih kuat, dan hubungan yang lebih baik dengan pelanggan dan mitra bisnis.
  </p>
  <p>
    Dengan mengikuti ISO 9001:2015, organisasi dapat menciptakan lingkungan yang mendukung perbaikan berkelanjutan dan memastikan bahwa mereka dapat memenuhi kebutuhan pelanggan dengan cara yang konsisten dan dapat diandalkan.
  </p>

