  <h2>
    A. TENTANG SERTIFIKASI ISO 14001:2018
  </h2>
  <p>
    ISO 14001:2018 adalah standar internasional yang mengatur sistem manajemen lingkungan (SML) dalam organisasi. Standar ini dirancang untuk membantu organisasi mengelola dampak lingkungan dari kegiatan operasional mereka secara efektif dan berkelanjutan. ISO 14001:2018 menggantikan versi sebelumnya (ISO 14001:2004), dengan pembaruan yang memberikan pendekatan yang lebih strategis dan terintegrasi. Berikut adalah beberapa hal penting mengenai ISO 14001:2018.
  </p>

  <img src="{{ asset('img/gallery/sertifikasi-iso-14001-2018-di-pt-tirta-kencana-andal-tikad-1.png') }}" alt="Ilustrasi">
  <p class="caption">Sertifikasi ISO 14001:2018 di PT. Tirta Kencana Andal (1)</p>

  <h4>
    1. Pendekatan Berbasis Risiko
  </h4>
  <p>
    Salah satu pembaruan utama dalam ISO 14001:2018 adalah fokus yang lebih besar pada pendekatan berbasis risiko. Organisasi diminta untuk mengidentifikasi, mengevaluasi, dan mengelola risiko dan peluang yang berkaitan dengan dampak lingkungan dari kegiatan mereka. Hal ini bertujuan untuk mengurangi potensi kerusakan lingkungan serta menciptakan peluang untuk peningkatan berkelanjutan.
  </p>

  <h4>
    2. Kepemimpinan yang Kuat
  </h4>
  <p>
    Seperti dalam ISO 9001:2015, ISO 14001:2018 menekankan pentingnya peran kepemimpinan dalam sistem manajemen lingkungan. Manajemen puncak harus menunjukkan komitmen terhadap pengelolaan lingkungan dengan memberikan arahan yang jelas, sumber daya yang memadai, serta memastikan bahwa kebijakan dan sasaran lingkungan dipahami dan diikuti oleh seluruh organisasi.
  </p>

  <h4>
    3. Integrasi dengan Strategi Bisnis
  </h4>
  <p>
    ISO 14001:2018 mendorong organisasi untuk mengintegrasikan manajemen lingkungan ke dalam strategi bisnis mereka. Standar ini menekankan pentingnya mempertimbangkan faktor lingkungan dalam pengambilan keputusan strategis. Dengan mengaitkan manajemen lingkungan dengan tujuan bisnis secara keseluruhan, organisasi dapat memastikan bahwa keberlanjutan lingkungan menjadi bagian integral dari proses operasional mereka.
  </p>

  <h4>
    4. Partisipasi Pihak Terkait
  </h4>
  <p>
    ISO 14001:2018 mendorong organisasi untuk melibatkan pihak terkait, baik internal (seperti karyawan) maupun eksternal (seperti pelanggan, pemasok, dan regulator), dalam pengelolaan lingkungan. Keterlibatan pihak-pihak ini membantu organisasi memperoleh perspektif yang lebih luas, serta meningkatkan pemahaman dan kesadaran mengenai isu-isu lingkungan.
  </p>

  <img src="{{ asset('img/gallery/sertifikasi-iso-14001-2018-di-pt-tirta-kencana-andal-tikad-2.png') }}" alt="Ilustrasi">
  <p class="caption">Sertifikasi ISO 14001:2018 di PT. Tirta Kencana Andal (2)</p>

  <h4>
    5. Tujuan dan Kebijakan Lingkungan yang Jelas
  </h4>
  <p>
    Standar ini mewajibkan organisasi untuk menetapkan kebijakan lingkungan yang jelas, serta sasaran dan tujuan yang terukur untuk mengurangi dampak lingkungan. Tujuan-tujuan ini harus konsisten dengan komitmen organisasi terhadap keberlanjutan dan harus dapat dievaluasi secara teratur untuk memastikan pencapaian yang optimal.
  </p>

  <h4>
    6. Peningkatan Berkelanjutan
  </h4>
  <p>
    Seperti standar ISO lainnya, ISO 14001:2018 berfokus pada peningkatan berkelanjutan. Organisasi harus terus-menerus mengevaluasi dan meningkatkan kinerja lingkungan mereka untuk mengurangi dampak negatif terhadap lingkungan. Proses evaluasi ini termasuk pengawasan dan audit lingkungan, serta tindak lanjut untuk memastikan bahwa kebijakan dan tujuan lingkungan tetap relevan dan efektif.
  </p>

  <h4>
    7. Persyaratan Dokumentasi yang Lebih Fleksibel
  </h4>
  <p>
    ISO 14001:2018 memberikan fleksibilitas lebih besar dalam hal dokumentasi dibandingkan dengan versi sebelumnya. Organisasi tidak lagi diwajibkan untuk membuat dokumen formal yang rumit, melainkan dapat memilih untuk mengelola informasi dan bukti yang diperlukan dengan cara yang sesuai dengan ukuran dan kompleksitas organisasi.
  </p>

  <h4>
    8. Pengelolaan Sumber Daya Alam dan Pengurangan Limbah
  </h4>
  <p>
    Salah satu aspek penting dari ISO 14001:2018 adalah pengelolaan sumber daya alam dan pengurangan limbah. Organisasi diwajibkan untuk mengidentifikasi cara-cara untuk meminimalkan konsumsi sumber daya alam yang tidak terbarukan dan mengelola limbah dengan cara yang lebih ramah lingkungan, seperti daur ulang dan pengurangan emisi.
  </p>

  <img src="{{ asset('img/gallery/sertifikasi-iso-14001-2018-di-pt-tirta-kencana-andal-tikad-3.jpg') }}" alt="Ilustrasi">
  <p class="caption">Sertifikasi ISO 14001:2018 di PT. Tirta Kencana Andal (3)</p>

  <h4>
    9. Manajemen Perubahan
  </h4>
  <p>
    ISO 14001:2018 juga mengharuskan organisasi untuk memiliki sistem yang dapat mengidentifikasi dan mengelola perubahan yang dapat mempengaruhi kinerja lingkungan mereka. Ini mencakup perubahan yang dapat terjadi dalam proses, produk, atau layanan yang dapat mempengaruhi dampak lingkungan.
  </p>

  <h4>
    10. Kesimpulan
  </h4>
  <p>
    ISO 14001:2018 adalah alat penting bagi organisasi yang ingin mengelola dampak lingkungan mereka secara sistematis dan berkelanjutan. Dengan mengikuti standar ini, organisasi tidak hanya akan memenuhi persyaratan regulasi lingkungan tetapi juga dapat memperoleh manfaat ekonomi dan reputasi yang lebih baik. Penerapan sistem manajemen lingkungan yang efektif dapat membantu organisasi mengurangi biaya, meningkatkan efisiensi operasional, dan berkontribusi pada tujuan global dalam mengurangi perubahan iklim dan kerusakan lingkungan.
  </p>
  <p>
    Secara keseluruhan, ISO 14001:2018 memberikan kerangka kerja yang komprehensif bagi organisasi untuk mengelola dampak lingkungan mereka dengan cara yang lebih terstruktur dan proaktif, sambil mendukung tujuan bisnis yang berkelanjutan.
  </p>


